<template>
    <div
        class="ww-message-item"
        :class="{
            'ww-message-item--own': isOwnMessage,
            'ww-message-item--continued': sameSenderAsPrevious,
            'ww-message-item--continue-next': sameSenderAsNext,
        }"
    >
        <!-- Message content -->
        <div
            class="ww-message-item__content"
            :class="{ 'ww-message-item__content--own': isOwnMessage }"
            :style="messageStyles"
            @contextmenu.prevent="handleRightClick"
        >
            <!-- Sender name if first in group -->
            <div
                v-if="!sameSenderAsPrevious"
                class="ww-message-item__sender"
                :class="{ 'ww-message-item__sender--own': isOwnMessage }"
            >
                {{ message.userName }}
            </div>

            <!-- Message text -->
            <div class="ww-message-item__text">
                {{ message.text }}
            </div>

            <!-- Attachments if any -->
            <div v-if="formattedAttachments.length" class="ww-message-item__attachments">
                <div
                    v-for="(attachmentMeta, index) in formattedAttachments"
                    :key="attachmentMeta.attachment.id ?? index"
                    class="ww-message-item__attachment"
                    :class="{ 'ww-message-item__attachment--own': isOwnMessage }"
                    @click="handleAttachmentClick(attachmentMeta.attachment)"
                >
                    <!-- Image preview for image files -->
                    <div
                        v-if="attachmentMeta.isImage"
                        class="ww-message-item__attachment-preview"
                        :class="{ 'ww-message-item__attachment-preview--own': isOwnMessage }"
                    >
                        <img :src="attachmentMeta.attachment.url" :alt="attachmentMeta.attachment.name" />
                    </div>

                    <!-- File icon for non-image files -->
                    <div
                        v-else
                        class="ww-message-item__attachment-file"
                        :class="{ 'ww-message-item__attachment-file--own': isOwnMessage }"
                    >
                        <div class="ww-message-item__attachment-icon">
                            <svg
                                xmlns="http://www.w3.org/2000/svg"
                                width="24"
                                height="24"
                                viewBox="0 0 24 24"
                                fill="none"
                                stroke="currentColor"
                                stroke-width="2"
                                stroke-linecap="round"
                                stroke-linejoin="round"
                            >
                                <path d="M14 2H6a2 2 0 0 0-2 2v16a2 2 0 0 0 2 2h12a2 2 0 0 0 2-2V8z"></path>
                                <polyline points="14 2 14 8 20 8"></polyline>
                                <line x1="16" y1="13" x2="8" y2="13"></line>
                            </svg>
                        </div>
                        <div class="ww-message-item__attachment-info">
                            <div class="ww-message-item__attachment-name">{{ attachmentMeta.attachment.name }}</div>
                            <div v-if="attachmentMeta.formattedSize" class="ww-message-item__attachment-size">
                                {{ attachmentMeta.formattedSize }}
                            </div>
                        </div>
                    </div>
                </div>
            </div>

            <!-- Message timestamp -->
            <div class="ww-message-item__time">
                {{ formatMessageTime(message.timestamp) }}
            </div>
        </div>
    </div>
</template>

<script>
import { computed, inject } from 'vue';
import { formatTime } from '../utils/dateTimeFormatter';

/**
 * @module MessageItem
 * @description A component that displays a single message in the chat, including text, attachments, and metadata.
 */
export default {
    name: 'MessageItem',
    props: {
        /**
         * The message object to display.
         * @type {Object}
         */
        message: {
            type: Object,
            required: true,
        },
        /**
         * Whether the message is sent by the current user.
         * @type {boolean}
         */
        isOwnMessage: {
            type: Boolean,
            default: false,
        },
        /**
         * Whether the previous message has the same sender.
         * @type {boolean}
         */
        sameSenderAsPrevious: {
            type: Boolean,
            default: false,
        },
        /**
         * Whether the next message has the same sender.
         * @type {boolean}
         */
        sameSenderAsNext: {
            type: Boolean,
            default: false,
        },
        /**
         * The background color of received messages.
         * @type {string}
         */
        messageBgColor: {
            type: String,
            default: '#f1f5f9',
        },
        /**
         * The text color of received messages.
         * @type {string}
         */
        messageTextColor: {
            type: String,
            default: '#334155',
        },
        /**
         * The font size of received messages.
         * @type {string}
         */
        messageFontSize: {
            type: String,
            default: '0.875rem',
        },
        /**
         * The font weight of received messages.
         * @type {string}
         */
        messageFontWeight: {
            type: String,
            default: '400',
        },
        /**
         * The font family of received messages.
         * @type {string}
         */
        messageFontFamily: {
            type: String,
            default: 'inherit',
        },
        /**
         * The border of received messages.
         * @type {string}
         */
        messageBorder: {
            type: String,
            default: '1px solid #e2e8f0',
        },
        /**
         * The background color of sent messages.
         * @type {string}
         */
        ownMessageBgColor: {
            type: String,
            default: '#dbeafe',
        },
        /**
         * The text color of sent messages.
         * @type {string}
         */
        ownMessageTextColor: {
            type: String,
            default: '#1e40af',
        },
        /**
         * The font size of sent messages.
         * @type {string}
         */
        ownMessageFontSize: {
            type: String,
            default: '0.875rem',
        },
        /**
         * The font weight of sent messages.
         * @type {string}
         */
        ownMessageFontWeight: {
            type: String,
            default: '400',
        },
        /**
         * The font family of sent messages.
         * @type {string}
         */
        ownMessageFontFamily: {
            type: String,
            default: 'inherit',
        },
        /**
         * The border of sent messages.
         * @type {string}
         */
        ownMessageBorder: {
            type: String,
            default: '1px solid #bfdbfe',
        },
        /**
         * The border radius of received messages.
         * @type {string}
         */
        messageRadius: {
            type: String,
            default: '18px 18px 18px 18px',
        },
        /**
         * The border radius of sent messages.
         * @type {string}
         */
        ownMessageRadius: {
            type: String,
            default: '18px 18px 18px 18px',
        },
    },
    emits: [
        /**
         * Emitted when an attachment is clicked.
         * @param {Object} attachment - The attachment that was clicked.
         */
        'attachment-click',
        /**
         * Emitted when a message is right-clicked.
         * @param {Object} payload - The event payload.
         * @param {Object} payload.message - The message that was right-clicked.
         * @param {number} payload.elementX - The x-coordinate of the click relative to the chat root element.
         * @param {number} payload.elementY - The y-coordinate of the click relative to the chat root element.
         * @param {number} payload.viewportX - The x-coordinate of the click relative to the viewport.
         * @param {number} payload.viewportY - The y-coordinate of the click relative to the viewport.
         */
        'right-click',
    ],
    setup(props, { emit }) {
        const isEditing = inject(
            'isEditing',
            computed(() => false)
        );
        const chatRootEl = inject('chatRootEl', null);

        const dateTimeOptions = inject(
            'dateTimeOptions',
            computed(() => ({}))
        );

        const messageStyles = computed(() => {
            if (props.isOwnMessage) {
                return {
                    backgroundColor: props.ownMessageBgColor,
                    color: props.ownMessageTextColor,
                    fontSize: props.ownMessageFontSize,
                    fontWeight: props.ownMessageFontWeight,
                    fontFamily: props.ownMessageFontFamily,
                    border: props.ownMessageBorder,
                    '--message-radius': props.ownMessageRadius,
                };
            } else {
                return {
                    backgroundColor: props.messageBgColor,
                    color: props.messageTextColor,
                    fontSize: props.messageFontSize,
                    fontWeight: props.messageFontWeight,
                    fontFamily: props.messageFontFamily,
                    border: props.messageBorder,
                    '--message-radius': props.messageRadius,
                };
            }
        });

        /**
         * Checks if an attachment is an image file.
         * @param {Object} attachment - The attachment to check.
         * @returns {boolean} True if the attachment is an image file.
         */
        const isImageFile = attachment => {
            if (!attachment.type) return false;
            return attachment.type.startsWith('image/');
        };

        /**
         * Formats a file size in bytes into a human-readable string.
         * @param {number} rawSize - The file size in bytes.
         * @returns {string} The formatted file size.
         */
        const formatFileSize = rawSize => {
            const bytes = Number(rawSize);
            if (!Number.isFinite(bytes) || bytes < 0) return '';
            if (bytes === 0) return '0 Bytes';

            const sizes = ['Bytes', 'KB', 'MB', 'GB', 'TB'];
            const index = Math.min(Math.floor(Math.log(bytes) / Math.log(1024)), sizes.length - 1);
            const value = bytes / Math.pow(1024, index);
            return `${parseFloat(value.toFixed(2))} ${sizes[index]}`;
        };

        const formattedAttachments = computed(() => {
            const attachments = Array.isArray(props.message.attachments) ? props.message.attachments : [];
            return attachments.map(attachment => ({
                attachment,
                isImage: isImageFile(attachment),
                formattedSize: formatFileSize(attachment.size),
            }));
        });

        /**
         * Formats a timestamp into a time string.
         * @param {Date|string|number} timestamp - The timestamp to format.
         * @returns {string} The formatted time string.
         */
        const formatMessageTime = timestamp => {
            return formatTime(timestamp, dateTimeOptions.value);
        };

        /**
         * Handles the click event on an attachment.
         * @param {Object} attachment - The clicked attachment.
         */
        const handleAttachmentClick = attachment => {
            if (isEditing.value) return;
            emit('attachment-click', attachment);
        };

        /**
         * Handles the right-click event on a message.
         * @param {MouseEvent} event - The mouse event.
         */
        const handleRightClick = event => {
            // Coordinates relative to the chat root element
            let elementX = 0;
            let elementY = 0;
            const root = chatRootEl && chatRootEl.value ? chatRootEl.value : null;
            if (root && typeof root.getBoundingClientRect === 'function') {
                const chatRect = root.getBoundingClientRect();
                elementX = event.clientX - chatRect.left;
                elementY = event.clientY - chatRect.top;
            } else {
                // Fallback: treat client coords as element-relative if root not found
                elementX = event.clientX;
                elementY = event.clientY;
            }

            // Coordinates relative to page top-left
            const viewportX = event.pageX;
            const viewportY = event.pageY;

            emit('right-click', {
                message: props.message,
                elementX,
                elementY,
                viewportX,
                viewportY,
            });
        };

        return {
            messageStyles,
            isImageFile,
            formatFileSize,
            formattedAttachments,
            formatMessageTime,
            handleAttachmentClick,
            handleRightClick,
        };
    },
};
</script>

